package com.examples.iterator;

/**
 * A classe "Valor" guarda uma string e um inteiro apenas para demonstrar a iteração de um objeto
 * com algum grau de complexidade.
 */
public class Valor {

    public int numero;
    public String nome;

    public Valor(int numero, String nome) {
        this.numero = numero;
        this.nome = nome;
    }

}
